#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	l;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	l = ((n % 10) + '0');
	ft_putchar_fd(l, fd);
	return ;
}
